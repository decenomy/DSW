    hash.SetHex(request.params[0].get_str());

    isminefilter filter = ISMINE_SPENDABLE_ALL;
    if (request.params.size() > 1)
        if (request.params[1].get_bool())
            filter = filter | ISMINE_WATCH_ONLY;

